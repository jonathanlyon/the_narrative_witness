import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import path from "node:path";
import { parseCsv } from "./lib/csv.mjs";

const repositoryRoot = path.resolve(
  path.dirname(fileURLToPath(import.meta.url)),
  ".."
);
const sourcePath = path.join(
  repositoryRoot,
  "content",
  "reader-recognition.csv"
);
const outputPath = path.join(
  repositoryRoot,
  "src",
  "data",
  "readerComments.ts"
);

const isIncluded = (value) =>
  !["false", "no", "0"].includes(String(value).trim().toLowerCase());

export async function generateReaderComments() {
  const rows = parseCsv(await readFile(sourcePath, "utf8"));
  const ids = new Set();

  const comments = rows
    .filter((row) => isIncluded(row.included))
    .map((row, index) => {
      const id = Number.parseInt(row.id, 10);

      if (!Number.isInteger(id) || id < 1) {
        throw new Error(`Row ${index + 2} has an invalid id.`);
      }
      if (ids.has(id)) {
        throw new Error(`Duplicate Recognition id: ${id}.`);
      }
      if (!row.display_name?.trim() || !row.comment?.trim()) {
        throw new Error(`Row ${index + 2} needs a display name and comment.`);
      }

      ids.add(id);

      return {
        id,
        name: row.display_name.trim(),
        comment: row.comment.trim(),
        ...(row.date?.trim() ? { date: row.date.trim() } : {}),
        ...(row.source?.trim() ? { source: row.source.trim() } : {}),
        ...(row.source_title?.trim()
          ? { sourceTitle: row.source_title.trim() }
          : {}),
        ...(row.source_url?.trim()
          ? { sourceUrl: row.source_url.trim() }
          : {})
      };
    })
    .sort((left, right) => left.id - right.id);

  const serializeComment = (comment) => {
    const lines = [
      `    id: ${comment.id},`,
      `    name: ${JSON.stringify(comment.name)},`,
      `    comment: ${JSON.stringify(comment.comment)},`
    ];

    for (const key of ["date", "source", "sourceTitle", "sourceUrl"]) {
      if (comment[key]) {
        lines.push(`    ${key}: ${JSON.stringify(comment[key])},`);
      }
    }

    lines[lines.length - 1] = lines.at(-1).replace(/,$/, "");
    return `  {\n${lines.join("\n")}\n  }`;
  };

  const file = `// Generated by npm run recognition:generate.
// Edit content/reader-recognition.csv instead of this file.

export interface ReaderComment {
  id: number;
  name: string;
  comment: string;
  date?: string;
  source?: string;
  sourceTitle?: string;
  sourceUrl?: string;
}

export const READER_COMMENTS: ReaderComment[] = [
${comments.map(serializeComment).join(",\n")}
];
`;

  await writeFile(outputPath, file, "utf8");
  return comments.length;
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const count = await generateReaderComments();
  console.log(`Generated ${count} Recognition entries.`);
}
